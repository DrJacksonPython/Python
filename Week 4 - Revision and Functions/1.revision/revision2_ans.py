myName = "Ziggy" #create a variable myName to hold a STRING with your name
print(myName[1])    #print the second letter of your name
print(myName[3])    #print the fourth letter of your name

print("Hello " + myName + ", nice to meet you")    #print a message to yourself - use + to put strings together

myNameCaps = myName.upper()#use .upper() to convert your name to capital letters
                        
print(myNameCaps)  #print your name in capital letters

myNameLower = myName.lower()    #create a variable myNameLower and assign it your name in lower case

print(myNameLower)#print your name in lower case letters
